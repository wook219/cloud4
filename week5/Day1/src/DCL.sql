-- GRANT
GRANT 권한 ON 테이블명 TO 유저이름;

-- REVOKE
REVOKE 권한 ON 테이블명 FROM 유저이름;

-- 어떤 권한들이 있는가?
-- CONNECT
    -- DB or 스키마에 연결하는 권한
-- SELECT
    -- DB에서 데이터를 검색할 수 있는 권한
-- INSERT
    -- DB에서 데이터를 삽임할 수 있는 권한
-- UPDATE
    -- DB에서 데이터를 업데이트할 수 있는 권한
-- DELETE
    -- DB에서 데이터를 삭제할 수 있는 권한
-- USAGE
    -- 스키마 또는 함수와 같은 데이터베이스 개체를 사용할 수 있는 권한